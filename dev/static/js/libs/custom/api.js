define(['ajax','baseSet'],function(a,b){
	return{
		login:function(suc){
			a.ajaxPost({
				url:b.postServer+'login',
				success:function(data){
					suc(data);
				}
			});
		},
		getProvince:function(suc){
			a.ajaxPost({
				url:b.postServer+'getProvinceList',
				success:function(data){
					suc(data);
				}
			});
		},
		getCity:function(id,suc){
			a.ajaxPost({
				url:b.postServer+'getCityList',
				data:{
					'provinceId':parseInt(id)
				},
				success:function(data){
					suc(data);
				}
			});
		},
		binding:function(data,suc){
			a.ajaxPost({
				url:b.postServer+'register',
				data:data,
				success:function(data){
					suc(data);
				}
			});
		},
		draw:function(suc){
			a.ajaxPost({
				url:b.postServer+'draw',
				success:function(data){
					suc(data);
				}
			});
		},
		getWinnerList:function(suc){
			a.ajaxPost({
				url:b.postServer+'getWinnerList',
				success:function(data){
					suc(data);
				}
			});
		},
		sendCode:function(suc){
			a.ajaxPost({
				url:b.postServer+'sendCode',
				success:function(data){
					suc(data);
				}
			});
		}
	};
});
